public class TriangleEquilater {
    // Atributs
    public double base;
    public double altura;
    
    // Mètodes
    public double calculaArea() {
        return (base * altura)  / 2;
    }
    
    public double calculaPerimetre() {
        return 3 * base;
    }
 
}
