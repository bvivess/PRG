public class Employee {
    private String name;

    public Employee(String name) {
        this.name = name;
    }
    
    public String getName() {
        return this.name;
    }
    
    /**
     * gets details about an Employee
     * @return String
     */
    public String getDetails() {
        return this.name;
    }
            
}